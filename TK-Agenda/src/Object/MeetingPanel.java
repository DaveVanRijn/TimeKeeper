    private void btnDeleteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMousePressed
        if (evt.getButton() == MouseEvent.BUTTON1) {
            btnDelete.setIcon(new ImageIcon(getClass().getResource("/Resource/exitIcon20x20Clicked.png")));
            deletePressed = true;
        }
    }//GEN-LAST:event_btnDeleteMousePressed

    private void btnDeleteMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseReleased
        btnDelete.setIcon(new ImageIcon(getClass().getResource("/Resource/exitIcon20x20.png")));
        if (deletePressed) {
            deletePressed = false;
            int option = JOptionPane.showOptionDialog(null, "Weet je zeker dat je"
                    + " afspraak '" + meeting.getTitle() + "' wil verwijderen?",
                    "Bevestig", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, new Object[]{"Ja", "Nee"}, "Nee");

            if (option == JOptionPane.YES_OPTION) {
                deleteMeeting();
            }
        }
    }//GEN-LAST:event_btnDeleteMouseReleased

