    public MeetingPanel(Meeting meeting) {
        this();
        if (meeting != null) {
            this.meeting = meeting;
            SimpleDateFormat dateForm = new SimpleDateFormat("dd-MM-yy HH:mm");
            String start = dateForm.format(meeting.getStart());
            String end = dateForm.format(meeting.getEnd());

            lblTitle.setText(meeting.getTitle());
            lblTime.setText(start + " - " + end);
        } else {
            lblTitle.setText("Titel");
            lblTime.setText("Begin - Eindtijd");
            btnDelete.setVisible(false);
            btnDetails.setVisible(false);
            jSeparator1.setVisible(false);
        }
    }

